SELECT
    tc.id AS company_id,
    ef.id AS emission_factor_id,
    s.activity_date,
    s.year_month,
    s.activity_type,
    s.description,
    ROUND((s.quantity * tc.multiplier * pm.scope_multiplier)::numeric, 4) AS quantity,
    s.unit,
    ef.source,
    ef.scope,
    ef.factor_kg,
    ROUND((s.quantity * tc.multiplier * pm.scope_multiplier * ef.factor_kg)::numeric, 4) AS emissions_kg,
    ROUND((s.quantity * tc.multiplier * pm.scope_multiplier * ef.factor_kg / 1000)::numeric, 4) AS emissions_tco2e,
    'seed-activity-data.xlsx'::text AS import_file_name,
    s.import_row_number
FROM tmp_target_activity_companies tc
CROSS JOIN seed_rows s
JOIN emission_factors ef
  ON ef.activity_type = s.activity_type
 AND ef.description = s.description
 AND ef.unit = s.unit
 AND ef.valid_from <= s.activity_date
 AND (ef.valid_to IS NULL OR ef.valid_to >= s.activity_date)
JOIN tmp_activity_scope_profiles p ON p.company_id = tc.id
CROSS JOIN LATERAL (
    SELECT CASE s.scope
        WHEN 1 THEN p.scope1_multiplier
        WHEN 2 THEN p.scope2_multiplier
        ELSE p.scope3_multiplier
    END AS scope_multiplier
) pm
ORDER BY tc.name ASC, s.import_row_number ASC;

-- Derive 2026 YTD activity records from the seeded 2025 Jan-May activity rows.
INSERT INTO activity_records (
    company_id,
    emission_factor_id,
    activity_date,
    year_month,
    activity_type,
    description,
    quantity,
    unit,
    source,
    scope,
    emission_factor_kg,
    emissions_kg,
    emissions_tco2e,
    import_file_name,
    import_row_number
)
SELECT
    ar.company_id,
    ef.id AS emission_factor_id,
    derived.activity_date,
    derived.year_month,
    ar.activity_type,
    ar.description,
    ar.quantity,
    ar.unit,
    ef.source,
    ef.scope,
    ef.factor_kg,
    ROUND((ar.quantity * ef.factor_kg)::numeric, 4) AS emissions_kg,
    ROUND((ar.quantity * ef.factor_kg / 1000)::numeric, 4) AS emissions_tco2e,
    'seed-activity-data-2026-ytd.xlsx' AS import_file_name,
    ar.import_row_number
FROM activity_records ar
CROSS JOIN LATERAL (
    SELECT
        TO_DATE(CONCAT('2026-', RIGHT(ar.year_month, 2), '-01'), 'YYYY-MM-DD') AS activity_date,
        CONCAT('2026-', RIGHT(ar.year_month, 2)) AS year_month
) derived
JOIN emission_factors ef
  ON ef.activity_type = ar.activity_type
 AND ef.description = ar.description
 AND ef.unit = ar.unit
 AND ef.valid_from <= derived.activity_date
 AND (ef.valid_to IS NULL OR ef.valid_to >= derived.activity_date)
WHERE ar.import_file_name = 'seed-activity-data.xlsx'
  AND ar.year_month BETWEEN '2025-01' AND '2025-05'
ORDER BY ar.company_id ASC, ar.import_row_number ASC;

