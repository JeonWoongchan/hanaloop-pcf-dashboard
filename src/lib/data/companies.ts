// 기준 연도 배출량에 연도 및 스케일 팩터를 적용해 다년 데이터 생성
function scaleToYear(
    base: GhgEmission[],
    year: string,
    defaultScale: number,
    sourceScales?: Partial<Record<string, number>>
): GhgEmission[] {
    return base.map((e) => ({
        ...e,
        yearMonth: `${year}-${e.yearMonth.slice(5)}`,
        emissions: Math.max(1, Math.round((sourceScales?.[e.source] ?? defaultScale) * e.emissions)),
    }));
}

